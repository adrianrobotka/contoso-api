            <p>Many EULAs assert extensive liability limitations. Most commonly, an EULA will attempt to hold harmless
                the software licensor in the event that the software causes damage to the user's computer or data, but
                some software also proposes limitations on whether the licensor can be held liable for damage that
                arises through improper use of the software (for example, incorrectly using tax preparation software and
                incurring penalties as a result). One case upholding such limitations on consequential damages is M.A.
                Mortenson Co. v. Timberline Software Corp., et al. Some EULAs also claim restrictions on venue and
                applicable law in the event that a legal dispute arises.</p>

            <p>Some copyright owners use EULAs in an effort to circumvent limitations the applicable copyright law
